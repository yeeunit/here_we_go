package com.here_you_go.project.model;

public class MemberVO {

	private String memId;
	private String memPwd;
	private String memEmail;
	private String memName;
	private String memBirth;
	private String memTelcom;
	private String memGender;
	private String memNation;
	private String memHp;
	
	
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getMemPwd() {
		return memPwd;
	}
	public void setMemPwd(String memPwd) {
		this.memPwd = memPwd;
	}
	public String getMemEmail() {
		return memEmail;
	}
	public void setMemEmail(String memEmail) {
		this.memEmail = memEmail;
	}
	public String getMemName() {
		return memName;
	}
	public void setMemName(String memName) {
		this.memName = memName;
	}
	public String getMemBirth() {
		return memBirth;
	}
	public void setMemBirth(String memBirth) {
		this.memBirth = memBirth;
	}
	public String getMemTelcom() {
		return memTelcom;
	}
	public void setMemTelcom(String memTelcom) {
		this.memTelcom = memTelcom;
	}
	public String getMemGender() {
		return memGender;
	}
	public void setMemGender(String memGender) {
		this.memGender = memGender;
	}
	public String getMemNation() {
		return memNation;
	}
	public void setMemNation(String memNation) {
		this.memNation = memNation;
	}
	public String getMemHp() {
		return memHp;
	}
	public void setMemHp(String memHp) {
		this.memHp = memHp;
	}
}

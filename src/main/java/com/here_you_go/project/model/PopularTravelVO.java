package com.here_you_go.project.model;

public class PopularTravelVO {
	public String popNo;
	public String popName;
	public String popInfo;
	public String popImg;
	
	
	public String getPopNo() {
		return popNo;
	}
	public void setPopNo(String popNo) {
		this.popNo = popNo;
	}
	public String getPopName() {
		return popName;
	}
	public void setPopName(String popName) {
		this.popName = popName;
	}
	public String getPopInfo() {
		return popInfo;
	}
	public void setPopInfo(String popInfo) {
		this.popInfo = popInfo;
	}
	public String getPopImg() {
		return popImg;
	}
	public void setPopImg(String popImg) {
		this.popImg = popImg;
	}
	
	
}

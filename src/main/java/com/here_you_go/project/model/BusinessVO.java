package com.here_you_go.project.model;

public class BusinessVO {

	private String comNo;
	private String comId;
	private String comPwd;
	private String comEmail;
	private String comName;
	private String comTelcom;
	private String comHP;
	
	
	public String getComNo() {
		return comNo;
	}
	public void setComNo(String comNo) {
		this.comNo = comNo;
	}
	public String getComId() {
		return comId;
	}
	public void setComId(String comId) {
		this.comId = comId;
	}
	public String getComPwd() {
		return comPwd;
	}
	public void setComPwd(String comPwd) {
		this.comPwd = comPwd;
	}
	public String getComEmail() {
		return comEmail;
	}
	public void setComEmail(String comEmail) {
		this.comEmail = comEmail;
	}
	public String getComName() {
		return comName;
	}
	public void setComName(String comName) {
		this.comName = comName;
	}
	
	public String getComTelcom() {
		return comTelcom;
	}
	public void setComTelcom(String comTelcom) {
		this.comTelcom = comTelcom;
	}
	
	public String getComHP() {
		return comHP;
	}
	public void setComHP(String comHP) {
		this.comHP = comHP;
	}
}
